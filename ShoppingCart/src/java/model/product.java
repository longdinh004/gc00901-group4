package model;

/**
 *
 * @author DELL
 */
public class Product {

    private int id;
    private String name;
    private float price;

    public Product(int i, String n, float p) {
        this.id = i;
        this.name = n;
        this.price = p;
    }

    Product() {        
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }

}
